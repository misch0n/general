'use strict';
/*
 * Package the exact free-column table (ev-exp-exact.bin, produced by
 * build-ev-exp-exact.js --full) into a shippable JS asset:
 *   values → int16 at 1/SCALE-pt resolution, delta-encoded within each mask,
 *   gzipped, base64'd, embedded in ev-exp-exact-table.js. The app decodes it at
 *   load via DecompressionStream('gzip') and reconstructs V*(mask, up).
 *
 * Run:  node tools/pack-ev-exp-exact.js
 */
var fs = require('fs'), zlib = require('zlib'), path = require('path');
var SCALE = 50;                                   // 0.02-pt resolution (< the 0.05 analysis threshold)
var meta = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'ev-exp-exact.meta.json'), 'utf8'));
var src = fs.readFileSync(path.join(__dirname, '..', 'ev-exp-exact.bin'));

// read [u32 count][per mask: u32 mask, i16 lo, u16 len, len×f32]
var off = 0, n = src.readUInt32LE(off); off += 4;
var entries = [];
for (var i = 0; i < n; i++) {
  var mask = src.readUInt32LE(off); off += 4;
  var lo = src.readInt16LE(off); off += 2;
  var len = src.readUInt16LE(off); off += 2;
  var vals = new Float32Array(len);
  for (var t = 0; t < len; t++) { vals[t] = src.readFloatLE(off); off += 4; }
  entries.push({ mask: mask, lo: lo, len: len, vals: vals });
}

// pack: [u32 count][u16 scale][u16 ncat] then per mask u32 mask, i16 lo, u16 len, len×i16 delta
var size = 8 + entries.length * 8 + meta.values * 2;
var out = Buffer.alloc(size), o = 0;
out.writeUInt32LE(entries.length, o); o += 4;
out.writeUInt16LE(SCALE, o); o += 2;
out.writeUInt16LE(meta.ncat, o); o += 2;
entries.forEach(function (e) {
  out.writeUInt32LE(e.mask, o); o += 4;
  out.writeInt16LE(e.lo, o); o += 2;
  out.writeUInt16LE(e.len, o); o += 2;
  var prev = 0;
  for (var t = 0; t < e.len; t++) { var cur = Math.round(e.vals[t] * SCALE); out.writeInt16LE(cur - prev, o); o += 2; prev = cur; }
});
var gz = zlib.gzipSync(out, { level: 9 });
var b64 = gz.toString('base64');
var js =
'/* AUTO-GENERATED by tools/pack-ev-exp-exact.js — do not edit by hand.\n' +
'   EXACT (mask, upper-subtotal) free-column value table, penalty-aware.\n' +
'   par V*(0,0) = ' + meta.par.toFixed(4) + ' ; ' + meta.values + ' values @ 1/' + SCALE + '-pt; gzip+b64.\n' +
'   Decode: base64 → DecompressionStream("gzip") → reconstruct per-mask Float arrays. */\n' +
'(function (root, factory) {\n' +
'  if (typeof module === "object" && module.exports) module.exports = factory();\n' +
'  else root.GeneralEVExactExp = factory();\n' +
'})(typeof self !== "undefined" ? self : this, function () {\n' +
'  return { par: ' + meta.par.toFixed(4) + ', scale: ' + SCALE + ', ncat: ' + meta.ncat + ', gz: "' + b64 + '" };\n' +
'});\n';
var outPath = path.join(__dirname, '..', 'ev-exp-exact-table.js');
fs.writeFileSync(outPath, js);
console.log('wrote ' + outPath + ' (' + (js.length / 1e6).toFixed(2) + ' MB JS; gz payload ' + (gz.length / 1e6).toFixed(2) + ' MB; par ' + meta.par.toFixed(2) + ')');
